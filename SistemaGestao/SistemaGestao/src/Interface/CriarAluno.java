    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    try {
        Alunos aluno = new Alunos();
        AlunosDAO dao = new AlunosDAO();

        // 1. Pega o estado do checkbox "Aluno ativo"
        aluno.setAtivo(jCheckBox1.isSelected());

        // 2. Pega dados dos campos de texto
        aluno.setNome(jTextField3.getText());
        aluno.setCpf(jTextField1.getText());
        aluno.setEmail(jTextField2.getText());

        // 3. Converte dataNascimento (string para LocalDate)
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate dataNasc = LocalDate.parse(jTextField4.getText(), formatter);
        aluno.setDataNascimento(dataNasc);

        // 4. Pega o curso selecionado no combo
        Cursos cursoSelecionado = (Cursos) jComboBox1.getSelectedItem();
        aluno.setCurso(cursoSelecionado);

        // 5. Salva no banco de dados via DAO
        dao.create(aluno);

        // 6. Feedback para usuÃ¡rio
        javax.swing.JOptionPane.showMessageDialog(this, "Aluno cadastrado com sucesso!", "Sucesso", javax.swing.JOptionPane.INFORMATION_MESSAGE);

        // 7. Limpar campos para novo cadastro
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jComboBox1.setSelectedIndex(0);
        jCheckBox1.setSelected(false);

    } catch (IllegalArgumentException | IllegalStateException e) {
        // ExceÃ§Ãµes de validaÃ§Ã£o customizadas em Alunos
        javax.swing.JOptionPane.showMessageDialog(this, "Erro de validaÃ§Ã£o: " + e.getMessage(), "Erro de Entrada", javax.swing.JOptionPane.ERROR_MESSAGE);
    } catch (java.time.format.DateTimeParseException e) {
        // Erro no formato da data
        javax.swing.JOptionPane.showMessageDialog(this, "Erro no formato da data! Use dd/MM/yyyy.", "Erro de Entrada", javax.swing.JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        // Outros erros inesperados
        javax.swing.JOptionPane.showMessageDialog(this, "Ocorreu um erro inesperado: " + e.getMessage(), "Erro", javax.swing.JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    }
    }//GEN-LAST:event_jButton1ActionPerformed

